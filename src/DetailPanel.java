  public void setData()
  {
       try
        {
            Class.forName("java.sql.DriverManager");
            Connection con = (Connection)DriverManager.getConnection ("jdbc:mysql://localhost:3306/microsoftexam", "root", "");
            Statement stmt = (Statement)con.createStatement();
            String query =" select * from studentlogin where D like "+id;
            //String query="SELECT name  FROM studentlogin "+"WHERE id='"+"141112043"+"';";
//String query =" select name from studentlogin where id like "+this.id;
            ResultSet rs=stmt.executeQuery(query);
            
            //rs.first();
            if(rs.next())
            {
                //JOptionPane.showMessageDialog(this,rs.getString("name") );
                String username=rs.getString("C");
                String mailid=rs.getString("E");
                String cno=rs.getString("F");
                if(cno!=null)
                    contactNo.setText(cno);
                
                mailId.setText(mailid);
                name.setText(username);
                ExamPanel.myName.setText(username);
                ExamPanel.myScholar.setText(id);
            }
            
               
           
        }
        catch (SQLException ex)
        {
            Logger.getLogger(DetailPanel.class.getName()).log(Level.SEVERE, null, ex);
        } 
        catch (ClassNotFoundException ex)
        {
            Logger.getLogger(DetailPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
         
      
      
  }
  
