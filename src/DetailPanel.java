    public void insertDataToRecord()
    {
        String sch,nam,gender,cno,mail,address;
        sch=ScholarNo.getText();
        nam=name.getText();
        gender=Male.isSelected()?"Male":"Female";
        cno=contactNo.getText();
        mail=mailId.getText();
        address=Address.getText();
        try
        {
            Class.forName("java.sql.DriverManager");
            Connection con = (Connection)DriverManager.getConnection ("jdbc:mysql://localhost:3306/microsoftexam", "root", "");
            Statement stmt = (Statement)con.createStatement();
            String query="INSERT INTO record VALUES('"+sch+"','"+nam+"'   ,  '"+gender+ "','"+cno+"','"+mail+"','"+address+"'   );";
           // JOptionPane.showMessageDialog(this, query);
            stmt.executeUpdate(query);
            con.close();
        }
        catch (SQLException ex)
        {
            Logger.getLogger(DetailPanel.class.getName()).log(Level.SEVERE, null, ex);
        } 
        catch (ClassNotFoundException ex)
        {
            Logger.getLogger(DetailPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
